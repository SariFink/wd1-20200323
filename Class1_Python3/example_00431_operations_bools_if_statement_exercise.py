# you have 2 numbers, 5 and 10
# if the first is bigger than the second, print bigger
# if the second is bigger, print smaller
# otherwise print equal

number1 = 5
number2 = 10
if number1 > number2:
    print("bigger")
elif number1 < number2:
    print("smaller")
else:
    print("equal")


